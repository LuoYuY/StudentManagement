package manage.model;

public class NameScore {
	String luoyy_sno;
	String luoyy_sname;
	int luoyy_score;
	
	public NameScore() {
		super();
	}
	public NameScore(String luoyy_sno, String luoyy_sname, int luoyy_score) {
		super();
		this.luoyy_sno = luoyy_sno;
		this.luoyy_sname = luoyy_sname;
		this.luoyy_score = luoyy_score;
	}
	/**
	 * @return the luoyy_sno
	 */
	public String getLuoyy_sno() {
		return luoyy_sno;
	}
	/**
	 * @param luoyy_sno the luoyy_sno to set
	 */
	public void setLuoyy_sno(String luoyy_sno) {
		this.luoyy_sno = luoyy_sno;
	}
	/**
	 * @return the luoyy_sname
	 */
	public String getLuoyy_sname() {
		return luoyy_sname;
	}
	/**
	 * @param luoyy_sname the luoyy_sname to set
	 */
	public void setLuoyy_sname(String luoyy_sname) {
		this.luoyy_sname = luoyy_sname;
	}
	/**
	 * @return the luoyy_score
	 */
	public int getLuoyy_score() {
		return luoyy_score;
	}
	/**
	 * @param luoyy_score the luoyy_score to set
	 */
	public void setLuoyy_score(int luoyy_score) {
		this.luoyy_score = luoyy_score;
	}
	
	
}
